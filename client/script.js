// Create a contract instance
const contract = new web3.eth.Contract(contractABI, CONTRACT_ADDRESS);

// Function to query the total supply of the token
async function getTotalSupply() {
  try {
    const totalSupply = await contract.methods.totalSupply().call();
    console.log("Total Supply:", totalSupply);
  } catch (error) {
    console.error("Error fetching total supply:", error);
  }
}

// Function to mint the token
async function mintTokens(to, amount) {
  const amountBN = web3.utils.toBN(amount);
  const mintFunction = contract.methods.mint(to,amountBN);

  web3.eth.accounts
    .signTransaction(
      {
        to: CONTRACT_ADDRESS,
        data: mintFunction.encodeABI(),
        gas: "3000000",
        gasPrice: web3.utils.toWei("10", "gwei"),
        nonce: await web3.eth.getTransactionCount(PUBLIC_KEY),
      },
      PRIVATE_KEY
    )
    .then((signedTx) => {
      web3.eth
        .sendSignedTransaction(signedTx.rawTransaction)
        .on("transactionHash", function (hash) {
          console.log("Transaction hash:", hash);
        })
        .on("error", function (error) {
          console.error("Error:", error);
        });
    })
    .catch((error) => {
      console.error("Error signing transaction:", error);
    });
}


